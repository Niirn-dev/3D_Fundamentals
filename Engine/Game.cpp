#include "MainWindow.h"
#include "Game.h"
#include "Mat2.h"
#include "Mat3.h"

Game::Game( MainWindow& wnd )
	:
	wnd( wnd ),
	gfx( wnd )
{
}

void Game::Go()
{
	gfx.BeginFrame();
	UpdateModel();
	ComposeFrame();
	gfx.EndFrame();
}

void Game::UpdateModel()
{
}

void Game::ComposeFrame()
{
	Vec3 v = { 1.0f,2.0f,3.0f };
	Mat3 m = { 
		10.0f,9.0f,1.0f,
		7.0f,12.0f,13.0f,
		1.0f,0.0f,12.0f
	};

	v *= m;
}